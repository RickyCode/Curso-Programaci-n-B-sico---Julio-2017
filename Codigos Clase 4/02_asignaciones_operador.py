# Modificamos los valores de una variable numérica de la forma larga:
numero = 100 # Asignamos el número 100 a la variable "numero"
print('Valor de la variable número: ', numero)
numero = numero + 50
print('Nuevo valor de la variable número: ', numero)
print() # Esto hace un salto de linea.


# Modificamos la variable de la forma más corta:
numero = 100
print('Valor de la variable número: ', numero)
numero += 50
print('Nuevo valor de la variable número: ', numero)
print()


# Realizamos varios cambios con distintos operadores:
numero = 5
print('Numero: ', numero)
numero += 2
print('Numero después de la suma: ', numero)
numero *= 2
print('Numero después de la multiplicación: ', numero)
numero /= 2
print('Numero después de la división: ', numero)
numero -= 2
print('Numero después de la resta: ', numero)
numero %= 2
print('Numero después del módulo: ', numero)
numero **= 2
