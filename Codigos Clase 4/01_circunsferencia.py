pi = 3.14159265359 # Una aproximación del número pi
r = 50             # Radio de una circunsferencia
# La fórmula del perimetro de la circunsferencia es 2 veces pi por el radio.
perimetro_circunsferencia = 2* pi * r
# La fórmula del área es pi por el radio al cuadrado.
area_circunsferencia = pi*(r**2)
# Mostramos el área y el perímetro al usuario:
print('El área del círculo es: ',area_circunsferencia)
print('El perímetro de la circunsferencia es: ', perimetro_circunsferencia)


